export function buildPrompt({
  category,
  difficulty,
  count = 5,
  customPrompt,
}: {
  category?: Category
  difficulty?: Difficulty
  count: number
  customPrompt?: string
}): string {
  let prompt = BASE_PROMPT

  // 追加のプロンプトを設定
  prompt += `\n\n${AI_PROMPT}`

  // カテゴリが指定されている場合
  if (category && CATEGORY_PROMPTS[category]) {
    prompt += `\n\n【カテゴリ指定】\n${CATEGORY_PROMPTS[category]}`
  }

  // 難易度が指定されている場合
  if (difficulty && DIFFICULTY_PROMPTS[difficulty]) {
    prompt += `\n\n【難易度設定】\n${DIFFICULTY_PROMPTS[difficulty]}`
  }

  // カスタムプロンプトが指定されている場合
  if (customPrompt) {
    prompt += `\n\n【追加要望】\n${customPrompt}`
  }

  // 生成数を指定
  prompt += `\n\n${count}個のお題を生成してください。`

  return prompt
}

export function parseOdaiResponse(response: string): string[] {
  // 改行で分割し、空行や番号付きの行を処理
  const lines = response
    .split('\n')
    .map((line) => line.trim())
    .filter((line) => line.length > 0)
    .map((line) => {
      // 番号や記号を除去 (例: "1. ", "・", "- ")
      return line
        .replace(/^[\d]+[.)]\s*/, '')
        .replace(/^[・\-*]\s*/, '')
        .replace(/^[\d]+\s*/, '')
        .trim()
    })
    .filter((line) => line.length > 0 && line.length <= 200) // 空行と長すぎる行を除外

  return lines
}
